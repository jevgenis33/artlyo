export function getArtworkBySlug(slug: string) {
  return artworks.find((a) => a.slug === slug);
}

export function getArtworksByArtist(artistId: string) {
  return artworks.filter((a) => a.artistId === artistId);
}

export function getFeaturedArtworks() {
  return artworks.filter((a) => a.featured && a.status === "available");
}

export function filterArtworks(options: {
  category?: string;
  search?: string;
  minPrice?: number;
  maxPrice?: number;
}) {
  return artworks.filter((a) => {
    if (options.category && a.category !== options.category) return false;
    if (options.minPrice && a.price < options.minPrice) return false;
    if (options.maxPrice && a.price > options.maxPrice) return false;
    if (options.search) {
      const q = options.search.toLowerCase();
      const match =
        a.title.toLowerCase().includes(q) ||
        a.description.toLowerCase().includes(q);
      if (!match) return false;
    }
    return true;
  });
}
